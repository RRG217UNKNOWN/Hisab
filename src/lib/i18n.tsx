const DICTS: Record<Lang, Dict> = { en, hi, mr };

export type ContentMode = "words" | "numbers" | "both";

// Hindi and Marathi both use Devanagari-script numerals.
const DEVANAGARI_DIGITS = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"];
function toDevanagariDigits(input: string | number): string {
  return String(input).replace(/[0-9]/g, (d) => DEVANAGARI_DIGITS[Number(d)]);
}

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
  contentMode: ContentMode;
  setContentMode: (m: ContentMode) => void;
  t: (key: string) => string;
  /** Fills `{placeholders}` in a translated template, e.g.
   * tf("act.addedItem", { name: "Parle-G" }) -> `added item "Parle-G"`. */
  tf: (key: string, vars: Record<string, string | number>) => string;
  /** Formats a number/numeric string according to the content-mode setting
   * (e.g. converts "1,234" to "१,२३४" when numerals should be localised). */
  formatNumber: (n: number | string) => string;
};

const I18nContext = createContext<Ctx>({
  lang: "en",
  setLang: () => {},
  contentMode: "both",
  setContentMode: () => {},
  t: (k) => en[k] ?? k,
  tf: (k, vars) => (en[k] ?? k).replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? "")),
  formatNumber: (n) => String(n),
});

const LOCAL_KEY = "bahi.lang";
const CONTENT_MODE_KEY = "bahi.contentMode";

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    if (typeof window === "undefined") return "en";
    const s = localStorage.getItem(LOCAL_KEY);
    return s === "hi" || s === "mr" || s === "en" ? s : "en";
  });
  const [contentMode, setContentModeState] = useState<ContentMode>(() => {
    if (typeof window === "undefined") return "both";
    const s = localStorage.getItem(CONTENT_MODE_KEY);
    return s === "words" || s === "numbers" || s === "both" ? s : "both";
  });

  // Hydrate from profile once we know the user
  useEffect(() => {
    let cancelled = false;
    (async () => {
      const { data: { user } } = await supabase.auth.getUser();
      if (!user || cancelled) return;
      const { data } = await supabase
        .from("profiles")
        .select("language")
        .eq("id", user.id)
        .maybeSingle();
      const l = data?.language;
      if (!cancelled && (l === "en" || l === "hi" || l === "mr")) {
        setLangState(l);
        if (typeof window !== "undefined") localStorage.setItem(LOCAL_KEY, l);
      }
    })();
    const { data: sub } = supabase.auth.onAuthStateChange((_e, s) => {
      if (!s?.user) return;
      supabase
        .from("profiles")
        .select("language")
        .eq("id", s.user.id)
        .maybeSingle()
        .then(({ data }) => {
          const l = data?.language;
          if (l === "en" || l === "hi" || l === "mr") {
            setLangState(l);
            if (typeof window !== "undefined") localStorage.setItem(LOCAL_KEY, l);
          }
        });
    });
    return () => {
      cancelled = true;
      sub.subscription.unsubscribe();
    };
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") localStorage.setItem(LOCAL_KEY, l);
    // persist to profile
    supabase.auth.getUser().then(({ data: { user } }) => {
      if (!user) return;
      supabase.from("profiles").update({ language: l }).eq("id", user.id).then(() => {});
    });
  };

  // Content mode (words / numbers / both) is a lighter-weight, per-device
  // display preference — kept in localStorage only, no profile round-trip.
  const setContentMode = (m: ContentMode) => {
    setContentModeState(m);
    if (typeof window !== "undefined") localStorage.setItem(CONTENT_MODE_KEY, m);
  };

  const value = useMemo<Ctx>(
    () => ({
      lang,
      setLang,
      contentMode,
      setContentMode,
      t: (key: string) => {
        const translateWords = contentMode === "words" || contentMode === "both";
        return translateWords ? (DICTS[lang][key] ?? en[key] ?? key) : (en[key] ?? key);
      },
      tf: (key: string, vars: Record<string, string | number>) => {
        const translateWords = contentMode === "words" || contentMode === "both";
        const template = translateWords ? (DICTS[lang][key] ?? en[key] ?? key) : (en[key] ?? key);
        return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
      },
      formatNumber: (n: number | string) => {
        const translateNumbers = contentMode === "numbers" || contentMode === "both";
        return translateNumbers && lang !== "en" ? toDevanagariDigits(n) : String(n);
      },
    }),
    [lang, contentMode],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT() {
  return useContext(I18nContext);
}
